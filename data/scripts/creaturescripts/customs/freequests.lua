local function playerFreeQuestSetup(playerId, index)
    
	local player = Player(playerId)
	if not player then
        return
    end

    for i = 1, 5 do
        index = index + 1
        if not questTable[index] then
            player:sendTextMessage(MESSAGE_INFO_DESCR, "Player FreeQuest is complete.")
            player:setStorageValue(Storage.FreeQuests, upperLimit)
            return
        end

        if player:getStorageValue(questTable[index].storage) ~= questTable[index].questTable then
            player:setStorageValue(questTable[index].storage, questTable[index].questTable)
        end
    end

    addEvent(playerFreeQuestSetup, 500, playerId, index)
end

local freeQuests = CreatureEvent("FreeQuests")

function freeQuests.onLogin(player)
    if configManager.getBoolean(configKeys.FREE_QUESTS) and 
        player:getStorageValue(Storage.FreeQuests) == upperLimit then
        return true
    end

    player:sendTextMessage(MESSAGE_INFO_DESCR, "Player FreeQuest in progress..")
    addEvent(playerFreeQuestSetup, 500, player:getId(), 0)
    return true
end

